package string;

public class contains {

	public static void main(String[] args) {
     String name = "my name is Harsh Rai, and I am a proud Indian";
     System.out.println(name.contains("my name is"));
     System.out.println(name.contains(","));
     System.out.println(name.contains("wow"));
     System.out.println(name.contains("a"));
	}

}
